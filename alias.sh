echo 'alias c="clear"' >> ~/.bashrc
echo 'alias overkill="cd /media/sf_overkill/"' >> ~/.bashrc
echo 'alias home="cd ~/"' >> ~/.bashrc
echo 'alias showbash="less ~/.bashrc"' >> ~/.bashrc
echo 'alias bashconfig="nano ~/.bashrc"' >> ~/.bashrc
echo 'alias s="source ~/.bashrc"' >> ~/.bashrc
